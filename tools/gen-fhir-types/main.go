// Command gen-fhir-types generates Go structs from the FHIR R5 JSON Schema.
//
// Usage:
//
//	go run tools/gen-fhir-types/main.go -schema fhir.schema.json -out core/fhir/r5/types.go
//
// The input schema file is the FHIR R5 JSON Schema (fhir.schema.json) published
// by HL7 at https://hl7.org/fhir/R5/fhir.schema.json.
//
// Only resources specified with -resources (comma-separated) are generated.
// Common types (Meta, Coding, CodeableConcept, etc.) are always included.
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"regexp"
	"sort"
	"strings"
)

// Schema represents the top-level FHIR JSON Schema structure.
type Schema struct {
	Definitions map[string]*SchemaDef `json:"$defs"`
}

// SchemaDef represents a single type/resource definition.
type SchemaDef struct {
	Description string              `json:"description"`
	Type        string              `json:"type"`
	Properties  map[string]*PropDef `json:"properties"`
	AllOf       []*SchemaRef        `json:"allOf"`
	Required    []string            `json:"required"`
}

// PropDef describes a single property.
type PropDef struct {
	Type        string              `json:"type"`
	Description string              `json:"description"`
	Ref         string              `json:"$ref"`
	Items       *PropDef            `json:"items"`
	OneOf       []*SchemaRef        `json:"oneOf"`
	Required    []string            `json:"required"`
}

// SchemaRef is a $ref reference.
type SchemaRef struct {
	Ref string `json:"$ref"`
}

